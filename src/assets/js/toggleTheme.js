theme_icon.addEventListener("click", () =>{
  if(localStorage.getItem('theme')=='dark'){
   
    themeselect('light')
  }else{
    
   themeselect('dark')
  }
});

var current_time = new Date().getHours();

if (current_time > 12) {
  themeselect('dark');
} else {
  themeselect('light');
}
